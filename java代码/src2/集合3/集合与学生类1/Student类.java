package 集合3.集合与学生类1;

public class Student类 {
    private String name;
    private int age;

    public Student类(){
    }

    public Student类(String name, int age){
        this.name = name;
        this.age = age;
    }

    public void setName(String name){
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age){
        this.age = age;
    }

    public int getAge() {
        return age;
    }
}
